package de.mysh.calcapp.util;

public class Calculator {

    public static int summarise (int num1, int num2) {
        return num1 + num2;
    }

    public static int subtract (int num1, int num2) {
        return num1 - num2;
    }

    public static int multiply (int num1, int num2) {
        return num1 * num2;
    }
}
